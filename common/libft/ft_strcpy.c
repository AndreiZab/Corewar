#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	len;

	len = ft_strlen(src);
	ft_memcpy((void*)dst, (void*)src, len);
	*(dst + len) = '\0';
	return (dst);
}
